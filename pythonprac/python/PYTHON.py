x=10
y=9.8
z=5+3j
a="Prasansha"

#type function
print("type function")
print(type(x),"\n")
print(type(y),"\n")
print(type(z),"\n")
print(type(a),"\n")
#type conversion
print("type conversion")
print(int(y),"\n")
print(float(x),"\n")
print(complex(x),"\n")
#id function
print("id function")
print(id(x),"\n")
print(id(y),"\n")
print(id(z),"\n")
print(id(a),"\n")
#slicing
print("slicing concept")
print(a[2:],"\n")
print(a[0:7],"\n")
print(a[0:9:2],"\n")
print(a[ ::-1],"\n")
print(len(a),"\n")
      

       

